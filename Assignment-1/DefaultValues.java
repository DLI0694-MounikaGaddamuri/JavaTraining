public class DefaultValues{
    static int intNumber;
    static float floatNumber;
    static char charVar;
    static double doubleNumber;
    static byte byteNumber;
    static short shortNumber;
    static boolean bool;
    public static void main(String[] args){
        System.out.println("Default values of Primitive data types:");
        System.out.println("int: "+intNumber);
        System.out.println("float: "+floatNumber);
        System.out.println("char: "+charVar);
        System.out.println("double: "+doubleNumber);
        System.out.println("byte: "+byteNumber);
        System.out.println("short: "+shortNumber);
        System.out.println("boolean: "+bool);
  }
}
